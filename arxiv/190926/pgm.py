pgm.add_edge('x','xHat')
pgm.add_edge('p_omega','omega')
pgm.add_edge('omega','R')
pgm.add_edge('B','R')
pgm.add_edge('beta','f')
pgm.add_edge('f','R')
pgm.add_edge('f','tau')
pgm.add_edge('k','R')
pgm.add_edge('k','tau')
pgm.add_edge('ITI','tau')
pgm.add_edge('R','tau')
pgm.add_edge('R','G')
pgm.add_edge('tau','G')
pgm.add_edge('R','rho')
pgm.add_edge('tau','rho')

pgm.render()
pgm.figure.show()